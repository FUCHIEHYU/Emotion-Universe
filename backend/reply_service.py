import random

def pick_reply(emotion, topic, need):
    emotion_group = REPLY_TEMPLATES.get(emotion, {})
    topic_group = emotion_group.get(topic) or emotion_group.get("general", {})
    replies = topic_group.get(need) or topic_group.get("comfort") or ["謝謝你願意分享心情。"]
    return random.choice(replies)

def add_intensity_tail(reply, intensity):
    if intensity == "high":
        return reply + " 先不用逼自己立刻整理好一切，現在能先照顧好自己就很重要。"
    return reply

def generate_reply(slots, text):
    emotion = slots.get("emotion", "sad")
    topic = slots.get("topic", "general")
    need = slots.get("need", "comfort")
    intensity = slots.get("intensity", "normal")

    reply = pick_reply(emotion, topic, need)
    reply = add_intensity_tail(reply, intensity)
    reply = add_keyword_touch(text, reply)  

    return reply

def add_keyword_touch(text, reply):
    if "不知道怎麼辦" in text:
        reply += " 當你覺得不知道怎麼辦的時候，也代表你真的已經撐了一段時間了。"
    if "好累" in text:
        reply += " 你說累，表示你真的不是沒努力，而是已經撐很久了。"
    if "想哭" in text:
        reply += " 想哭不是脆弱，很多時候只是心裡累到需要一個出口。"
    return reply

